/**
 * An enum representing the available instructions and their binary codes.
 * @author Erik Burmester <erik.burmester@nextbeam.net>
 */
export enum OpCode {
	ADD = 0b00000000,
	ADC = 0b00000001,
	SUB = 0b00000010,
	SBB = 0b00000011,
	MUL = 0b00000100,
	DIV = 0b00000101,
	NEG = 0b00000110,
	CMP = 0b00000111,
	TEST = 0b00001000,
	JMP = 0b00001001,
	JZ = 0b00001010,
	JE = 0b00001011,
	JNZ = 0b00001100,
	JNE = 0b00001101,
	JG = 0b00001110,
	JGE = 0b00001111,
	JL = 0b00010000,
	JLE = 0b00010001,
	MOV = 0b00010010,
	LEA = 0b00010011,
	POPF = 0b00010100,
	PUSHF = 0b00010101,
	CLC = 0b00010110,
	CMC = 0b00010111,
	STC = 0b00011000,
	CLI = 0b00011001,
	STI = 0b00011010,
	INT = 0b00011011,
	IRET = 0b00011100,
	AND = 0b00011101,
	OR = 0b00011110,
	XOR = 0b00011111,
	NOT = 0b00100000,
	CALL = 0b00100001,
	RET = 0b00100010,
	POP = 0b00100011,
	PUSH = 0b00100100,
	SYSENTER = 0b00100101,
	SYSEXIT = 0b00100110,
	DEV = 0b00100111,
	SHL = 0b00101000, // SHL = SAL
	SHR = 0b00101001,
	SAR = 0b00101010,
	INVTLB = 0b00101011,
	JA = 0b00101100,
	JAE = 0b00101101,
	JB = 0b00101110,
	JBE = 0b00101111,
	NOP = 0b11111111
}