/**
 * An enum representing the available instructions and their binary codes.
 * @author Erik Burmester <erik.burmester@nextbeam.net>
 */
export enum InstructionSet {
	ADD = 0b0000000,
	ADC = 0b0000001,
	SUB = 0b0000010,
	SBB = 0b0000011,
	MUL = 0b0000100,
	DIV = 0b0000101,
	NEG = 0b0000110,
	CMP = 0b0000111,
	TEST = 0b0001000,
	JMP = 0b0001001,
	JZ = 0b0001010,
	JE = 0b0001011,
	JNZ = 0b0001100,
	JNE = 0b0001101,
	JG = 0b0001110,
	JGE = 0b0001111,
	JL = 0b0010000,
	JLE = 0b0010001,
	MOV = 0b0010010,
	LEA = 0b0010011,
	POPF = 0b0010100,
	PUSHF = 0b0010101,
	CLC = 0b0010110,
	CMC = 0b0010111,
	STC = 0b0011000,
	CLI = 0b0011001,
	STI = 0b0011010,
	INT = 0b0011011,
	IRET = 0b0011100,
	AND = 0b0011101,
	OR = 0b0011110,
	XOR = 0b0011111,
	NOT = 0b0100000,
	CALL = 0b0100001,
	RET = 0b0100010,
	POP = 0b0100011,
	PUSH = 0b0100100,
	SYSENTER = 0b0100101,
	SYSEXIT = 0b0100110,
	DEV = 0b0100111,
	SHL = 0b0101000, // SHL = SAL
	SHR = 0b0101001,
	SAR = 0b0101010,
	INVTLB = 0b0101011,
	JA = 0b0101100,
	JAE = 0b0101101,
	JB = 0b0101110,
	JBE = 0b0101111,
	NOP = 0b1111111
}